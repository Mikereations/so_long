#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	c = n % 10 + '0';
	if (n > 9)
	{
		n = n / 10 ;
		ft_putnbr_fd(n, fd);
	}
	ft_putchar_fd(c, fd);
}
